Le canal d'Otaru, autrefois vital pour le transport des marchandises, est aujourd'hui une attraction touristique majeure. Flâner le long de ses rives permet d'admirer l'architecture élégante des entrepôts restaurés, abritant désormais des boutiques, des cafés et des galeries d'art.
