<h1 class="sapporoh1">
    Sapporo
</h1>
<img class="sapporoimg" src="./images/sapporo.jpg" alt="">
<div class="sapporop123">
<p>
    Sapporo, la plus grande ville de l'île septentrionale du Japon, Hokkaido, est une métropole dynamique offrant une fusion unique de culture urbaine et de charme naturel. Célèbre pour sa bière, ses festivals de neige spectaculaires et sa délicieuse cuisine, Sapporo attire les visiteurs du monde entier.
    Au cœur de la ville se trouve le parc Odori, un vaste espace vert qui s'étend sur plusieurs kilomètres et offre une oasis de calme au milieu de l'agitation urbaine. En hiver, le parc se transforme en le site principal du Festival de la neige de Sapporo, où des sculptures de glace monumentales illuminées enchantent les visiteurs.
    Sapporo est également réputée pour sa scène culinaire vibrante. Les rues regorgent de restaurants proposant des spécialités locales telles que le délicieux ramen au miso, les succulentes brochettes de crabe et les célèbres "Genghis Khan", des grillades de mouton hokkaidoï.
</p>
